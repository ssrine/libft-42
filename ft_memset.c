#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*pr;
	size_t	i;

	i = 0;
	pr = (char *)str;
	while (n)
	{
		pr[i] = c;
		i++;
		n--;
	}
	return (str);
}
