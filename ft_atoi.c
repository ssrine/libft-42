#include "libft.h"

int	ft_atoi(const char *str)
{
	int		m;
	size_t	r;
	int		i;

	i = 0;
	m = 1;
	r = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m = m * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = (r * 10) + (str[i] - 48);
		i++;
	}
	if (r > 9223372036854775807 && m == 1)
		return (-1);
	else if (r > 9223372036854775807 && m == -1)
		return (0);
	return (r * m);
}
